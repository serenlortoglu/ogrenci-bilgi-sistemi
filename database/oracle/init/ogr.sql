
CREATE TABLE USERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR2(50) UNIQUE NOT NULL,
    PASSWORD VARCHAR2(100) NOT NULL,
    ROLE VARCHAR2(20) CHECK (ROLE IN ('ADMIN','TEACHER','STUDENT')) NOT NULL
);

CREATE TABLE STUDENTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STUDENT_NO VARCHAR2(20) UNIQUE NOT NULL,
    FIRST_NAME VARCHAR2(50) NOT NULL,
    LAST_NAME VARCHAR2(50) NOT NULL,
    DEPARTMENT VARCHAR2(100),
    CLASS_LEVEL NUMBER(1),
    EMAIL VARCHAR2(100),
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE'
);

CREATE TABLE TEACHERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TEACHER_NO VARCHAR2(20) UNIQUE NOT NULL,
    FIRST_NAME VARCHAR2(50) NOT NULL,
    LAST_NAME VARCHAR2(50) NOT NULL,
    DEPARTMENT VARCHAR2(100),
    EMAIL VARCHAR2(100),
    TITLE VARCHAR2(50)
);

CREATE TABLE COURSES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COURSE_CODE VARCHAR2(20) UNIQUE NOT NULL,
    COURSE_NAME VARCHAR2(100) NOT NULL,
    CREDITS NUMBER(2),
    TEACHER_ID NUMBER,
    CONSTRAINT FK_COURSE_TEACHER
        FOREIGN KEY (TEACHER_ID) REFERENCES TEACHERS(ID)
);

CREATE TABLE GRADES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STUDENT_ID NUMBER,
    COURSE_ID NUMBER,
    MIDTERM NUMBER(3),
    FINAL NUMBER(3),
    LETTER_GRADE VARCHAR2(2),
    CONSTRAINT FK_GRADES_STUDENT
        FOREIGN KEY (STUDENT_ID) REFERENCES STUDENTS(ID),
    CONSTRAINT FK_GRADES_COURSE
        FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID)
);

CREATE TABLE ATTENDANCE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STUDENT_ID NUMBER,
    COURSE_ID NUMBER,
    WEEK_NO NUMBER(2),
    STATUS VARCHAR2(10) CHECK (STATUS IN ('PRESENT','ABSENT')),
    CONSTRAINT FK_ATT_STUDENT
        FOREIGN KEY (STUDENT_ID) REFERENCES STUDENTS(ID),
    CONSTRAINT FK_ATT_COURSE
        FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID)
);
